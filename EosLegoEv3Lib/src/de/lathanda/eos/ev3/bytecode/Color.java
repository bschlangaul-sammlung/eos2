package de.lathanda.eos.ev3.bytecode;

public interface Color {
	byte NONE   = (byte)0x01; 
	byte BLACK  = (byte)0x01; 
	byte BLUE   = (byte)0x02;
	byte GREEN  = (byte)0x03; 
	byte YELLOW = (byte)0x04; 
	byte RED    = (byte)0x05; 
	byte WHITE  = (byte)0x06; 
	byte BROWN  = (byte)0x07;
}
